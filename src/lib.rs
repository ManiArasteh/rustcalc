pub fn add(first: usize, second: usize) -> usize {
    first + second
}

pub fn remove(first: usize, second: usize) -> usize {
    first - second
}

pub fn multiply(first: usize, second: usize) -> usize {
    first * second
}

pub fn divide(first: usize, second: usize) -> usize {
    first / second
}

